package edu.kit.wavelength.client.view.update;

/**
 * Provides all available output formats.
 */
public enum OutputFormat {
	/**
	 * Representation of lambda terms using plain text and the unicode lambda
	 * symbol.
	 */
	UNICODE,
	/**
	 * Representation of lambda terms as syntax trees.
	 */
	TREE,
	/**
	 * SVG-based diagrams 
	 */
	PLUGDIAGRAM
}
